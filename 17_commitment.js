// ------------WHILE LOOP-------------
let num=1;
while(num<=60){
    console.log(num,'I will invest at least 6 hrs every single day for next 60 days!');
    num++;
}


// ------------FOR LOOP-------------
for(let i=1 ; i<=60 ; i++){
    console.log(i,'I will invest at least 6 hrs every single day for next 60 days!');
}